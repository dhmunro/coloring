"""Matched perceptually uniform sequential colormaps.

These are shaded gray scales, intended to display multiple data sets
side by side, for example densities of several different fluids in
contact with one another.  These are sequential maps, and pairs of
them work well as divergent maps.

Created using the viscm tool.

The seven maps are red, green, blue, cyan, purple, gold, gray,
corresponding to the primary and secondary colors RGB and CMYK (with
the more suggestive names purple and gold substituted for magenta and
yellow).  The are each perceptually uniform, meaning that equal
changes in level result in equal changes in perceptual difference in
color for all seven maps, according to the CIECAM02 color model.
Furthermore, at equal levels, the perceived brightness for all seven
maps are equal.  (Roughly speaking, if printed on a gray scale
printer, all seven maps are identical.)  Since the gray map is
perceptually uniform, that means all seven maps also have equal
changes in brightness for equal changes in level; they are uniform
both in brightness and in distinguishability of colors.

The interface consists of two objects `hue` and `hueset`.  To set the
colormap on the current figure, and make it the default for future
plots, use `hueset`.  To return the Colormap object (to pass to the
cmap keyword, for example), use `hue`.  The colormaps are properties
of the objects, and also items in the objects::

    imshow(image, cmap=hue.gold)  # or hue['gold']
    hueset.blue
    hueset.gray_r  # append _r to go from dark to light

You can also use any pair of maps to generate a divergent map with
either `hue` or `hueset`, for example::

    hueset.red_blue  # similar to coolwarm, perhaps superior
    hueset.red_r_blue  # dark in the middle (red_blue light in middle)

The most highly recommended maps are:

blue / gold / gray
  These three appear very similar to most colorblind people as to people
  with normal color vision.

"""

